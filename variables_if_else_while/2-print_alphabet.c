#include <stdio.h>
/**
 *main - Entry point of the program
 *
 *This program prints
 *
 *the lowercase alphabet
 *
 *using the same function
 *
 *in more than one expression
 *
 *Return: always 0
 */
int main(void)
{
	char c;

	for (c = 'a'; c <= 'z'; c++)

	{
		putchar(c);
	}
putchar('\n');

return (0);
}
